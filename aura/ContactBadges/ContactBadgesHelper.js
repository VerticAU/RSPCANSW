({
    showToast: function (component, title, type, recordId) {

        component.find('notifLib').showToast({
            "mode": 'sticky',
            "message": "{0}",
            "variant": type,
            "messageData": [
                {
                    url: '/' + recordId,
                    label: title
                }
            ]
        });
    }
})